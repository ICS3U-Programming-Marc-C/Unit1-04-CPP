// This program calculates the area and perimeter of a rectangle.

#include <iostream>
#include <cmath>
int main() {
    std::cout << "If a rectangle has the dimensions:" <<std::endl;
    std::cout << "6cm x 7cm"    << std::endl;
    std::cout << "The area is " << (6*7) << "cm^2." << std::endl;
    std::cout << "The perimeter is " << 2*(6+7) << "cm." << std::endl;
}
